# list
# create a list of numbers
numbers = [1, 2, 3, 4, 5]

# print the list
print(numbers)

# add a number to the end of the list
numbers.append(6)

# print the updated list
print(numbers)

# remove the first occurrence of a number from the list
numbers.remove(3)

# print the updated list
print(numbers)

# sort the list in ascending order
numbers.sort()

# print the sorted list
print(numbers)
